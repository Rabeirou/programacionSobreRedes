    /**
     * Almacena los 2 numeros ingresados por el usuario y con un trycatch verifica si los datos son validos.
     * Luego dependiendo de que radioButton este seleccionado se suma, resta, multiplica o divide.
     * Muestra el resultado de la operacion en un jLabel.
     * @param evt 
     */
    private void resultadoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resultadoButtonActionPerformed
        try{
            txtRes.setForeground(Color.BLACK);
            float num1 = Float.parseFloat(txtNum1.getText());
            float num2 = Float.parseFloat(txtNum2.getText());
            if(sumaRadio.isSelected()){
                txtRes.setText(String.valueOf(num1 + num2));
            }else if(restaRadio.isSelected()){
                txtRes.setText(String.valueOf(num1 - num2));
            }else if(multiplicacionRadio.isSelected()){
                txtRes.setText(String.valueOf(num1 * num2));
            }else if(divisionRadio.isSelected()){
                txtRes.setText(String.valueOf(num1 / num2));
            }else{
                txtRes.setText("");
            }
        } catch (NumberFormatException e){ 
            txtRes.setText("Error");
            txtRes.setForeground(Color.RED);
        }
    }//GEN-LAST:event_resultadoButtonActionPerformed
    
    /**
     * Asigna una tarea a un hilo para hacer funcionar el semaforo y que cambie de color.
     * Asigna otra tarea a otro hilo, la cual se basa en hacer que los autos se muevan luego del tiempo que tarda el semaforo.
     * Deshabilita el boton de iniciar, para que no se superpongan las tareas.
     * @param evt 
     */
    private void iniciarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarButtonActionPerformed
        tarea1 = new TareaSemaforo(rojoLabel, amarilloLabel, verdeLabel, 700);
        tarea2 = new TareaAutos(autoDerLabel, autoIzqLabel, 100, 1500);
        th1 = new Thread(tarea1);
        th2 = new Thread(tarea2);
        th1.start();
        th2.start();
        iniciarButton.setEnabled(false);
    }//GEN-LAST:event_iniciarButtonActionPerformed
    
